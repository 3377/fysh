# 配置SSHD
configure_sshd() {
    local sshd_config="/etc/ssh/sshd_config"
    local needs_restart=false
    local backup_file="${sshd_config}.backup.$(date +%Y%m%d_%H%M%S)"
    
    # 检查是否有root权限
    if [ "$(id -u)" -ne 0 ]; then
        error "配置SSHD需要root权限"
        return 1
    }
    
    # 备份原配置文件
    info "备份当前SSH配置..."
    cp "$sshd_config" "$backup_file"
    
    # 配置PubkeyAuthentication
    if ! grep -q "^PubkeyAuthentication yes" "$sshd_config"; then
        info "启用公钥认证..."
        # 注释掉所有PubkeyAuthentication行
        sed -i 's/^PubkeyAuthentication.*/# &/' "$sshd_config"
        # 添加新的配置
        echo "PubkeyAuthentication yes" >> "$sshd_config"
        needs_restart=true
    fi
    
    # 配置AuthorizedKeysFile
    if ! grep -q "^AuthorizedKeysFile.*\.ssh/authorized_keys" "$sshd_config"; then
        info "配置授权密钥文件路径..."
        # 注释掉所有AuthorizedKeysFile行
        sed -i 's/^AuthorizedKeysFile.*/# &/' "$sshd_config"
        # 添加新的配置
        echo "AuthorizedKeysFile .ssh/authorized_keys" >> "$sshd_config"
        needs_restart=true
    fi
    
    # 验证配置
    if ! sshd -t; then
        error "SSH配置验证失败，正在恢复备份..."
        cp "$backup_file" "$sshd_config"
        return 1
    fi
    
    # 如果需要，重启SSHD服务
    if [ "$needs_restart" = true ]; then
        info "正在重启SSH服务..."
        if command -v systemctl >/dev/null 2>&1; then
            systemctl restart sshd
        elif command -v service >/dev/null 2>&1; then
            service sshd restart
        else
            error "无法重启SSH服务，请手动重启"
            return 1
        fi
        success "SSH服务已重启"
    else
        info "SSH配置已是最新，无需重启"
    fi
    
    return 0
}

# 主程序
main() {
    # 检查参数
    if [ $# -eq 2 ]; then
        WEBDAV_USER="$1"
        WEBDAV_PASS="$2"
        
        # 测试WebDAV连接
        if ! test_webdav "$WEBDAV_USER" "$WEBDAV_PASS"; then
            exit 1
        fi
        
        # 清理现有配置
        clean_local_config
        
        # 初始化新的配置目录
        init_config
        
        # 从WebDAV下载配置
        info "从WebDAV同步配置..."
        if ! download_from_webdav "$WEBDAV_USER" "$WEBDAV_PASS"; then
            error "配置同步失败"
            exit 1
        fi
        
        # 授权当前主机
        authorize_current_host
        
        # 上传配置到WebDAV
        if ! upload_to_webdav "$WEBDAV_USER" "$WEBDAV_PASS"; then
            error "配置上传失败"
            exit 1
        fi
        
        # 显示交互式菜单
        handle_menu
    else
        show_help
        exit 1
    fi
}

main "$@"
